int x = 1500; //number

switch (x)
{
  case 10:
    Console.WriteLine("x = 10");
    break;
  case 50:
    Console.WriteLine("x = 50");
    break;
  case 500:
    Console.WriteLine("x = 500");
    break;
  case 1500:
    Console.WriteLine("x = 1500");
    break;
  case 5000:
    Console.WriteLine("x = 5000");
    break;
  default:
    Console.WriteLine("----------------");
    break;
}
